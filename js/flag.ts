enum Flag {
    N   = 1 << 7, //  1000 0000 | 0x80 | 128
    V   = 1 << 6, //  0100 0000 | 0x40 | 64 
    B   = 1 << 4, //  0001 0000 | 0x10 | 16
    D   = 1 << 3, //  0000 1000 | 0x08 | 8
    I   = 1 << 2, //  0000 0100 | 0x04 | 4
    Z   = 1 << 1, //  0000 0010 | 0x02 | 2
    C   = 1 << 0, //  0000 0001 | 0x01 | 1
    CLR = 1 << 5, //  0010 0000 | 0x20 | 32
}
Object.freeze(Flag);
module.exports = Flag;